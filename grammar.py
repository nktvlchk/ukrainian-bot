def get_gender_from_pos(pos: str) -> str:
    """Extract gender from part of speech string."""
    p = pos.lower()
    if "ч.р" in p or "ч." in p and "ж" not in p and "с" not in p:
        return "ч"
    elif "ж.р" in p or "ж." in p:
        return "ж"
    elif "с.р" in p or "с." in p and "спол" not in p:
        return "с"
    elif p.startswith("ім. ч"):
        return "ч"
    elif p.startswith("ім. ж"):
        return "ж"
    elif p.startswith("ім. с"):
        return "с"
    return ""


