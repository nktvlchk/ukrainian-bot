def conjugate_verb(word: str) -> dict:
    """
    Conjugate a Ukrainian verb in present tense.
    Input: infinitive form (e.g., "читати", "писати")
    Returns dict with person forms.
    """
    w = word.lower().strip()

    if not w.endswith(("ти", "тися")):
        return {}

    reflexive = w.endswith("тися") or w.endswith("ться")
    if reflexive:
        base_inf = w.replace("тися", "ти").replace("ться", "ти")
        suffix = "ся"
    else:
        base_inf = w
        suffix = ""

    # Remove -ти to get the stem
    stem = base_inf[:-2]

    # Determine conjugation pattern based on infinitive ending
    if base_inf.endswith("увати"):
        # -увати → -ую, -уєш... (купувати → купую)
        s = stem[:-3]  # remove "ува" from stem "купува"
        forms = {
            "я": s + "ую" + suffix,
            "ти": s + "уєш" + suffix,
            "він/вона": s + "ує" + suffix,
            "ми": s + "уємо" + suffix,
            "ви": s + "уєте" + suffix,
            "вони": s + "ують" + suffix,
        }
    elif base_inf.endswith("ювати"):
        # -ювати → -юю, -юєш...
        s = stem[:-3]  # remove "юва"
        forms = {
            "я": s + "юю" + suffix,
            "ти": s + "юєш" + suffix,
            "він/вона": s + "ює" + suffix,
            "ми": s + "юємо" + suffix,
            "ви": s + "юєте" + suffix,
            "вони": s + "юють" + suffix,
        }
    elif base_inf.endswith("ити"):
        # II conjugation: -ити → -лю, -иш, -ить... (робити → роблю)
        s = stem[:-1]  # remove "и" from stem — "роб" from "роби"
        # First person: consonant changes
        last = s[-1] if s else ""
        if last in ("б",):
            ya = s + "лю"
        elif last in ("д",):
            ya = s[:-1] + "джу"
        elif last in ("т",):
            ya = s[:-1] + "чу"
        elif last in ("з",):
            ya = s[:-1] + "жу"
        elif last in ("с",):
            ya = s[:-1] + "шу"
        elif last in ("в",):
            ya = s + "лю"
        elif last in ("п",):
            ya = s + "лю"
        elif last in ("м",):
            ya = s + "лю"
        elif last in ("ч",):
            ya = s + "у"
        elif last in ("ш", "щ", "ж"):
            ya = s + "у"
        elif last in ("н",):
            ya = s + "ю"
        elif last in ("р",):
            ya = s + "ю"
        elif last in ("л",):
            ya = s + "ю"
        else:
            ya = s + "лю"

        forms = {
            "я": ya + suffix,
            "ти": s + "иш" + suffix,
            "він/вона": s + "ить" + suffix,
            "ми": s + "имо" + suffix,
            "ви": s + "ите" + suffix,
            "вони": s + "лять" + suffix if last in ("б", "в", "п", "м") else s + "ять" + suffix,
        }
    elif base_inf.endswith("іти"):
        # -іти: ходити-style (but "іти" specifically)
        s = stem[:-1]  # remove "і"
        forms = {
            "я": s + "ю" + suffix if s.endswith(("д", "т", "с", "з")) else s + "лю" + suffix,
            "ти": s + "іш" + suffix,
            "він/вона": s + "іть" + suffix,
            "ми": s + "імо" + suffix,
            "ви": s + "іте" + suffix,
            "вони": s + "ять" + suffix,
        }
    elif base_inf.endswith("яти"):
        # -яти → -ю, -єш... (мріяти → мрію)
        # stem ends with 'я': мрія
        forms = {
            "я": stem + "ю" + suffix,
            "ти": stem + "єш" + suffix,
            "він/вона": stem + "є" + suffix,
            "ми": stem + "ємо" + suffix,
            "ви": stem + "єте" + suffix,
            "вони": stem + "ють" + suffix,
        }
    elif base_inf.endswith("ати"):
        # I conjugation: -ати → -аю, -аєш... (читати → читаю)
        # stem ends with 'а': чита
        forms = {
            "я": stem + "ю" + suffix,
            "ти": stem + "єш" + suffix,
            "він/вона": stem + "є" + suffix,
            "ми": stem + "ємо" + suffix,
            "ви": stem + "єте" + suffix,
            "вони": stem + "ють" + suffix,
        }
    elif base_inf.endswith("ути"):
        # -ути → -у, -еш (тягнути → тягну)
        forms = {
            "я": stem + "у" + suffix,
            "ти": stem + "еш" + suffix,
            "він/вона": stem + "е" + suffix,
            "ми": stem + "емо" + suffix,
            "ви": stem + "ете" + suffix,
            "вони": stem + "уть" + suffix,
        }
    else:
        # Fallback
        forms = {
            "я": stem + "у" + suffix,
            "ти": stem + "еш" + suffix,
            "він/вона": stem + "е" + suffix,
            "ми": stem + "емо" + suffix,
            "ви": stem + "ете" + suffix,
            "вони": stem + "уть" + suffix,
        }

    # Past tense (based on infinitive stem without -ти)
    past = {
        "він": stem + "в" + suffix,
        "вона": stem + "ла" + suffix,
        "воно": stem + "ло" + suffix,
        "вони": stem + "ли" + suffix,
    }

    # Imperative
    imperative = {
        "ти": stem + "й" + suffix if stem[-1:] in ("а", "і", "и", "я") else stem + "и" + suffix,
        "ви": stem + "йте" + suffix if stem[-1:] in ("а", "і", "и", "я") else stem + "іть" + suffix,
    }

    return {
        "infinitive": w,
        "present": forms,
        "past": past,
        "imperative": imperative,
    }


